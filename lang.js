function setLanguage(lang) {
  document.querySelectorAll("[data-key]").forEach((el) => {
    const key = el.getAttribute("data-key");
    const value = translations[lang]?.[key];
    if (!value) return;

    // ✅ Handle placeholder fields (inputs & textareas)
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.placeholder = value;
    } else if (value.includes("<")) {
      el.innerHTML = value;
    } else {
      el.textContent = value;
    }
  });

  localStorage.setItem("lang", lang);
}

// 3) DROPDOWN UI
function updateDropdownUI(lang) {
  const currentFlag = document.getElementById("langCurrentFlag");
  const currentLabel = document.getElementById("langCurrentLabel");
  const menuItems = document.querySelectorAll("#langMenu li");

  if (!currentFlag || !currentLabel) return;

  // Update button (current language)
  if (lang === "da") {
    currentFlag.src = "assets/dk.svg";
    currentFlag.alt = "Dansk";
    currentLabel.textContent = "DA";
  } else {
    currentFlag.src = "assets/gb.svg";
    currentFlag.alt = "English";
    currentLabel.textContent = "EN";
  }

  // Show ONLY the other language in dropdown
  menuItems.forEach((item) => {
    const itemLang = item.getAttribute("data-lang");
    item.style.display = itemLang === lang ? "none" : "flex";
  });
}

// 4) INIT + EVENTS
document.addEventListener("DOMContentLoaded", () => {
  const dropdown = document.getElementById("langDropdown");
  const currentBtn = document.getElementById("langCurrent");
  const menu = document.getElementById("langMenu");

  // If dropdown is not on this page (e.g. other pages), just apply language and stop
  const savedLang = localStorage.getItem("lang") || "en";
  setLanguage(savedLang);
  updateDropdownUI(savedLang);

  if (!dropdown || !currentBtn || !menu) return;

  // Open / Close dropdown
  currentBtn.addEventListener("click", (e) => {
    e.stopPropagation();
    dropdown.classList.toggle("open");
    currentBtn.setAttribute("aria-expanded", dropdown.classList.contains("open") ? "true" : "false");
  });

  // Select language
  menu.addEventListener("click", (e) => {
    const item = e.target.closest("li[data-lang]");
    if (!item) return;

    const lang = item.getAttribute("data-lang");
    setLanguage(lang);
    updateDropdownUI(lang);
    updateCvForLang(lang); // to update CV links immediately on selection
    dropdown.classList.remove("open");
    currentBtn.setAttribute("aria-expanded", "false");
  });

  // Close when clicking outside
  document.addEventListener("click", () => {
    dropdown.classList.remove("open");
    currentBtn.setAttribute("aria-expanded", "false");
  });

  // Close on Escape
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") {
      dropdown.classList.remove("open");
      currentBtn.setAttribute("aria-expanded", "false");
    }
  });
});

